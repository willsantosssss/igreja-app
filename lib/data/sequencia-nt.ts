export const getCapituloParaHoje = (): { livro: string; capitulo: number } => {
  const hoje = new Date();
  const anoAtual = hoje.getFullYear();
  const primeiroDeJaneiro = new Date(anoAtual, 0, 1);
  
  // Calcular dias desde 1º de janeiro
  const umDia = 24 * 60 * 60 * 1000;
  const diasDecorridos = Math.floor((hoje.getTime() - primeiroDeJaneiro.getTime()) / umDia);
  
  // Garantir que não ultrapasse o índice máximo
  const indice = Math.min(diasDecorridos, sequenciaNovoTestamento.length - 1);
  
  return sequenciaNovoTestamento[indice];
};

export const getTotalCapitulos = (): number => {
  return sequenciaNovoTestamento.length;
};

export const getCapituloByIndex = (index: number) => {
  return sequenciaNovoTestamento[Math.min(index, sequenciaNovoTestamento.length - 1)];
};
